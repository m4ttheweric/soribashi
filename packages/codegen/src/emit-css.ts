import type { ResolvedTheme, ThemeTokens, PartialThemeTokens, SemanticSurfaceValue } from '@soribashi/theme';
import type { EmitCssOptions } from './types.ts';
import { removeDefaultVariables } from './remove-default-variables.ts';
import { stripHslWrapper } from './hsl.ts';

const HEADER = `/* auto-generated by @soribashi/codegen — do not edit manually */`;

/**
 * Emits the soribashi theme as a CSS file with custom properties for the
 * configured scope and dark mode selectors.
 */
export function emitCss(theme: ResolvedTheme, opts: EmitCssOptions = {}): string {
  const effectiveTheme = opts.removeDefaultVariables ? removeDefaultVariables(theme) : theme;
  const additions = opts.cssVariablesResolver ? opts.cssVariablesResolver(effectiveTheme) : undefined;
  // 'auto' is treated as `true` at this layer; build.ts resolves 'auto' to a
  // concrete boolean before calling emitCss based on the Tailwind output mode.
  const emitCompanion = opts.emitCompanionHsl !== false;

  if (opts.removeDefaultVariables) {
    const removed =
      countEmittedTokenVars(theme, emitCompanion) -
      countEmittedTokenVars(effectiveTheme, emitCompanion);
    if (removed > 0) {
      // Nothing in soribashi ships a baseline stylesheet (no equivalent of
      // @mantine/core/styles.css), so deduped output is broken unless the
      // consumer provides one. Warn loudly until that story exists.
      console.warn(
        `[soribashi] removeDefaultVariables removed ${removed} variable(s) that match the soribashi defaults. ` +
          `The generated CSS is NOT self-contained: a baseline stylesheet defining the soribashi default ` +
          `variables must be loaded before it, or the removed variables will be undefined at runtime.`,
      );
    }
  }

  const lines: string[] = [HEADER, ''];

  // :root block
  lines.push(`${effectiveTheme.scope} {`);
  emitTokenLines(lines, effectiveTheme.tokens, emitCompanion);
  emitSemanticLines(lines, effectiveTheme, emitCompanion);
  if (additions?.root) {
    for (const [key, value] of Object.entries(additions.root)) {
      lines.push(`  ${key}: ${value};`);
    }
  }
  lines.push('}');

  // .dark block — emitted if there are dark token overrides OR consumer dark vars.
  // Emit into a scratch array first: composed themes materialize every dark
  // section as an empty object, so a key-count check on theme.dark would emit
  // an empty block.
  const darkLines: string[] = [];
  emitDarkTokenLines(darkLines, effectiveTheme.dark, emitCompanion);
  const hasDarkAdditions = !!additions?.dark && Object.keys(additions.dark).length > 0;
  if (darkLines.length > 0 || hasDarkAdditions) {
    lines.push('');
    lines.push(`${effectiveTheme.darkMode.selector} {`);
    lines.push(...darkLines);
    if (additions?.dark) {
      for (const [key, value] of Object.entries(additions.dark)) {
        lines.push(`  ${key}: ${value};`);
      }
    }
    lines.push('}');
  }

  // Optional scopes — flat blocks for each scope name
  if (additions?.scopes) {
    for (const [scopeName, scopeVars] of Object.entries(additions.scopes)) {
      if (scopeVars.root && Object.keys(scopeVars.root).length > 0) {
        lines.push('');
        lines.push(`${scopeName} {`);
        for (const [key, value] of Object.entries(scopeVars.root)) {
          lines.push(`  ${key}: ${value};`);
        }
        lines.push('}');
      }
      if (scopeVars.dark && Object.keys(scopeVars.dark).length > 0) {
        lines.push('');
        lines.push(`${effectiveTheme.darkMode.selector} ${scopeName} {`);
        for (const [key, value] of Object.entries(scopeVars.dark)) {
          lines.push(`  ${key}: ${value};`);
        }
        lines.push('}');
      }
    }
  }

  return `${lines.join('\n')}\n`;
}

function countEmittedTokenVars(theme: ResolvedTheme, emitCompanion: boolean): number {
  const scratch: string[] = [];
  emitTokenLines(scratch, theme.tokens, emitCompanion);
  emitDarkTokenLines(scratch, theme.dark, emitCompanion);
  return scratch.length;
}

function emitTokenLines(
  lines: string[],
  tokens: ThemeTokens,
  emitCompanion: boolean,
): void {
  for (const [family, scale] of Object.entries(tokens.colors).sort(byKey)) {
    for (const [shade, value] of Object.entries(scale).sort(byKey)) {
      lines.push(`  --color-${family}-${shade}: ${value};`);
      if (emitCompanion) {
        const bare = stripHslWrapper(value);
        if (bare !== null) {
          lines.push(`  --__hsl-color-${family}-${shade}: ${bare};`);
        }
      }
    }
  }

  for (const [key, value] of Object.entries(tokens.radius).sort(byKey)) {
    lines.push(`  --radius-${key}: ${value};`);
  }

  for (const [key, value] of Object.entries(tokens.spacing).sort(byKey)) {
    lines.push(`  --spacing-${key}: ${value};`);
  }

  for (const [key, value] of Object.entries(tokens.fontSize).sort(byKey)) {
    lines.push(`  --font-size-${key}: ${value};`);
  }

  if (tokens.fontFamily) {
    for (const [key, value] of Object.entries(tokens.fontFamily).sort(byKey)) {
      lines.push(`  --font-family-${key}: ${value};`);
    }
  }

  if (tokens.fontWeight) {
    for (const [key, value] of Object.entries(tokens.fontWeight).sort(byKey)) {
      lines.push(`  --font-weight-${key}: ${value};`);
    }
  }

  if (tokens.lineHeight) {
    for (const [key, value] of Object.entries(tokens.lineHeight).sort(byKey)) {
      lines.push(`  --line-height-${key}: ${value};`);
    }
  }

  if (tokens.shadow) {
    for (const [key, value] of Object.entries(tokens.shadow).sort(byKey)) {
      lines.push(`  --shadow-${key}: ${value};`);
    }
  }

  if (tokens.breakpoint) {
    for (const [key, value] of Object.entries(tokens.breakpoint).sort(byKey)) {
      lines.push(`  --breakpoint-${key}: ${value};`);
    }
  }

  if (tokens.zIndex) {
    for (const [key, value] of Object.entries(tokens.zIndex).sort(byKey)) {
      lines.push(`  --z-index-${key}: ${value};`);
    }
  }

  if (tokens.heading) {
    if (tokens.heading.textWrap !== undefined) {
      lines.push(`  --heading-text-wrap: ${tokens.heading.textWrap};`);
    }
    for (const [order, size] of Object.entries(tokens.heading.sizes).sort(byKey)) {
      lines.push(`  --heading-${order}-font-size: ${size.fontSize};`);
      if (size.fontWeight !== undefined) {
        lines.push(`  --heading-${order}-font-weight: ${size.fontWeight};`);
      }
      if (size.lineHeight !== undefined) {
        lines.push(`  --heading-${order}-line-height: ${size.lineHeight};`);
      }
    }
  }
}

function emitDarkTokenLines(
  lines: string[],
  dark: PartialThemeTokens,
  emitCompanion: boolean,
): void {
  if (dark.colors) {
    for (const [family, scale] of Object.entries(dark.colors).sort(byKey)) {
      for (const [shade, value] of Object.entries(scale ?? {}).sort(byKey)) {
        if (value !== undefined) {
          lines.push(`  --color-${family}-${shade}: ${value};`);
          if (emitCompanion) {
            const bare = stripHslWrapper(value);
            if (bare !== null) {
              lines.push(`  --__hsl-color-${family}-${shade}: ${bare};`);
            }
          }
        }
      }
    }
  }

  if (dark.radius) {
    for (const [key, value] of Object.entries(dark.radius).sort(byKey)) {
      if (value !== undefined) lines.push(`  --radius-${key}: ${value};`);
    }
  }

  if (dark.spacing) {
    for (const [key, value] of Object.entries(dark.spacing).sort(byKey)) {
      if (value !== undefined) lines.push(`  --spacing-${key}: ${value};`);
    }
  }

  if (dark.fontSize) {
    for (const [key, value] of Object.entries(dark.fontSize).sort(byKey)) {
      if (value !== undefined) lines.push(`  --font-size-${key}: ${value};`);
    }
  }

  if (dark.shadow) {
    for (const [key, value] of Object.entries(dark.shadow).sort(byKey)) {
      if (value !== undefined) lines.push(`  --shadow-${key}: ${value};`);
    }
  }

  if (dark.fontFamily) {
    for (const [key, value] of Object.entries(dark.fontFamily).sort(byKey)) {
      if (value !== undefined) lines.push(`  --font-family-${key}: ${value};`);
    }
  }

  if (dark.fontWeight) {
    for (const [key, value] of Object.entries(dark.fontWeight).sort(byKey)) {
      if (value !== undefined) lines.push(`  --font-weight-${key}: ${value};`);
    }
  }

  if (dark.lineHeight) {
    for (const [key, value] of Object.entries(dark.lineHeight).sort(byKey)) {
      if (value !== undefined) lines.push(`  --line-height-${key}: ${value};`);
    }
  }

  if (dark.breakpoint) {
    for (const [key, value] of Object.entries(dark.breakpoint).sort(byKey)) {
      if (value !== undefined) lines.push(`  --breakpoint-${key}: ${value};`);
    }
  }

  if (dark.zIndex) {
    for (const [key, value] of Object.entries(dark.zIndex).sort(byKey)) {
      if (value !== undefined) lines.push(`  --z-index-${key}: ${value};`);
    }
  }

  if (dark.heading) {
    if (dark.heading.textWrap !== undefined) {
      lines.push(`  --heading-text-wrap: ${dark.heading.textWrap};`);
    }
    if (dark.heading.sizes) {
      for (const [order, size] of Object.entries(dark.heading.sizes).sort(byKey)) {
        if (size === undefined) continue;
        if (size.fontSize !== undefined) {
          lines.push(`  --heading-${order}-font-size: ${size.fontSize};`);
        }
        if (size.fontWeight !== undefined) {
          lines.push(`  --heading-${order}-font-weight: ${size.fontWeight};`);
        }
        if (size.lineHeight !== undefined) {
          lines.push(`  --heading-${order}-line-height: ${size.lineHeight};`);
        }
      }
    }
  }
}

function emitSemanticLines(lines: string[], theme: ResolvedTheme, emitCompanion: boolean): void {
  for (const [key, ref] of Object.entries(theme.semanticTokens.text).sort(byKey)) {
    lines.push(`  --text-${key}: ${semanticToVar(ref)};`);
  }

  for (const [key, raw] of Object.entries(theme.semanticTokens.surface).sort(byKey)) {
    const pair = resolveSurfacePair(raw);
    lines.push(`  --surface-${key}: ${semanticToVar(pair.value)};`);
    if (emitCompanion) {
      const bare = resolveSemanticHsl(pair.value, theme);
      if (bare !== null) {
        lines.push(`  --__hsl-surface-${key}: ${bare};`);
      }
    }
    if (pair.foreground !== undefined) {
      lines.push(`  --surface-${key}-foreground: ${semanticToVar(pair.foreground)};`);
      if (emitCompanion) {
        const bare = resolveSemanticHsl(pair.foreground, theme);
        if (bare !== null) {
          lines.push(`  --__hsl-surface-${key}-foreground: ${bare};`);
        }
      }
    }
  }

  for (const [key, ref] of Object.entries(theme.semanticTokens.border).sort(byKey)) {
    lines.push(`  --border-${key}: ${semanticToVar(ref)};`);
  }

  if (theme.semanticTokens.accent) {
    for (const [key, ref] of Object.entries(theme.semanticTokens.accent).sort(byKey)) {
      lines.push(`  --accent-${key}: ${semanticToVar(ref)};`);
    }
  }
}

interface SurfacePair {
  value: string;
  foreground?: string;
}

function resolveSurfacePair(raw: SemanticSurfaceValue): SurfacePair {
  if (typeof raw === 'string') {
    return { value: raw };
  }
  return {
    value: raw.value,
    foreground: raw.foreground,
  };
}

/**
 * Resolves a semantic reference dot-path (e.g. `colors.neutral.500`) to a
 * `var(--__hsl-color-*)` reference for the underlying token's HSL companion.
 *
 * Returns `null` if the reference doesn't target a color token (e.g. radius),
 * or if the underlying token isn't an HSL value (so no `--__hsl-color-*`
 * companion was emitted for it).
 *
 * Emitting a var() reference rather than a literal HSL string ensures dark-mode
 * overrides cascade correctly: the `.dark` block already overrides
 * `--__hsl-color-*` (Wave 1 dual-emit), and semantic companions pick that up
 * automatically via the var() indirection.
 *
 * Used to emit `--__hsl-surface-{name}` Tailwind alpha companions alongside
 * the canonical `--surface-{name}: var(--color-*)` vars.
 */
function resolveSemanticHsl(ref: string, theme: ResolvedTheme): string | null {
  const parts = ref.split('.');
  if (parts[0] === 'colors' && parts.length === 3) {
    const family = parts[1]!;
    const shade = parts[2]!;
    const value = (theme.tokens.colors as Record<string, Record<string, string>>)[family]?.[shade];
    if (value === undefined) return null;
    // Only emit the companion if the underlying token is HSL (otherwise there's
    // no --__hsl-color-* var to reference).
    if (stripHslWrapper(value) === null) return null;
    return `var(--__hsl-color-${family}-${shade})`;
  }
  return null;
}

function semanticToVar(ref: string): string {
  // 'colors.neutral.500' -> 'var(--color-neutral-500)'
  // 'radius.md' -> 'var(--radius-md)'
  const parts = ref.split('.');
  if (parts[0] === 'colors' && parts.length === 3) {
    return `var(--color-${parts[1]}-${parts[2]})`;
  }
  if (parts[0] === 'radius' && parts.length === 2) {
    return `var(--radius-${parts[1]})`;
  }
  if (parts[0] === 'spacing' && parts.length === 2) {
    return `var(--spacing-${parts[1]})`;
  }
  if (parts[0] === 'fontSize' && parts.length === 2) {
    return `var(--font-size-${parts[1]})`;
  }
  return ref;
}

function byKey([a]: [string, unknown], [b]: [string, unknown]): number {
  return a.localeCompare(b);
}
